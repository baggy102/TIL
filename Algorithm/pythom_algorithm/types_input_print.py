# f-string
'''
파이썬 3.6 부터 사용 가능하며, 문자열 앞에 접두사 'f'를 붙여 사용한다.
중괄호 안에 변수명을 기입하여 간단히 문자열과 정수를 함께 넣을 수 있다.
'''

answer = 7
print(f"정답은 {answer}입니다.")
# -> 정답은 7입니다.
